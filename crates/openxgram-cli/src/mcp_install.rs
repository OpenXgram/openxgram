//! `xgram mcp install` + `xgram identity inject` — 사용자 / LLM 의 자율 셋업.
//!
//! 흐름:
//!   - mcp install: `.claude.json` (user scope) 또는 `.mcp.json` (project scope) 에
//!     openxgram 서버 등록. 파일이 없으면 생성. 이미 있으면 갱신.
//!   - identity inject: 현재 프로젝트 폴더의 `CLAUDE.md` 에 OpenXgram identity
//!     context 블록을 주입/갱신. marker 기반 (`<!-- OPENXGRAM:BEGIN -->`).
//!
//! 둘 다 idempotent — 반복 실행해도 안전. 자연어 대화로 LLM 이 호출 가능.

